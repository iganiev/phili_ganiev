#include "philo.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*buf;

	if (size && SIZE_MAX / size < count)
		return (NULL);
	buf = (void *)malloc(count * size);
	if (buf == NULL)
		return (buf);
	memset(buf, 0, count * size);
	return (buf);
}

static int	ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}

int	is_digit_string(const char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!ft_isdigit(str[i]))
			return (0);
		i++;
	}
	return (1);
}
